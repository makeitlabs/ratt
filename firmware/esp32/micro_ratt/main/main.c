#include <string.h>
#include <stdlib.h>
#include "freertos/FreeRTOS.h"
#include "freertos/task.h"
#include "freertos/event_groups.h"
#include "esp_wifi.h"
#include "esp_event.h"
#include "esp_log.h"
#include "esp_system.h"
#include "nvs_flash.h"
#include "esp_netif.h"

#include "lwip/err.h"
#include "lwip/sockets.h"
#include "lwip/sys.h"
#include "lwip/netdb.h"
#include "lwip/dns.h"

#include "esp_tls.h"
#include "esp_crt_bundle.h"

#include "sdcard.h"
#include "display_task.h"
#include "lcd_st7735.h"
#include "audio_task.h"
#include "net_task.h"
#include "rfid_task.h"


void app_main(void)
{
    ESP_ERROR_CHECK( nvs_flash_init() );
    ESP_ERROR_CHECK(esp_netif_init());
    ESP_ERROR_CHECK(esp_event_loop_create_default());

    rfid_init();
    sdcard_init();
    lcd_init_hw();
    display_init();

    audio_init();

    display_user_msg("Micro-RATT");

    //audio_play("/sdcard/macstart.s16");

    xTaskCreate(&rfid_task, "rfid_task", 4096, NULL, 8, NULL);
    xTaskCreate(&display_task, "display_task", 8192, NULL, 8, NULL);
    xTaskCreate(&audio_task, "audio_task", 4096, NULL, 10, NULL);

    xTaskCreate(&net_task, "net_task", 8192, NULL, 7, NULL);
}
